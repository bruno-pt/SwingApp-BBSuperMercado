    private void createListVendasGerais(){
        DefaultListModel listModel = new DefaultListModel();
        
        for(Caixa caixa: App.caixas){
            listModel.addElement("Caixa: "+(App.caixas.indexOf(caixa)+1)
                    +" | Valor: "+caixa.getValorAcumulado());
        }
        
        listVendasGerais.setModel(listModel);
    }
    
