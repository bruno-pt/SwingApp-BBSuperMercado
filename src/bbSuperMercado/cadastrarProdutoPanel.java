    private void jDialogError(String errorString, java.awt.event.ActionEvent evt){
        JDialog dialog = new JDialog();
        dialog.setSize(250, 100);
        dialog.setLayout(new FlowLayout());
        dialog.setDefaultCloseOperation(JDialog.DISPOSE_ON_CLOSE);

        JLabel errorLabel = new JLabel(errorString);
        JButton btnOk = new JButton("OK");                    

        btnOk.addActionListener(new ActionListener(){
            @Override
            public void actionPerformed(ActionEvent e) {                
                dialog.dispose();
                switch(errorString){
                    case "Preço inválido! Não é permitido Caracteres!":
                        tfPreco.setText("");
                        break;
                        
                    case "Quantidade inválida! Não é permitido valores decimais!":
                        tfQuantidade.setText("");
                        break;
                        
                    case "Nome do produto muito grande! Máx 30 caracteres!":
                        tfNome.setText("");
                        break;
                }                
            }
        }); 

        dialog.setTitle("Operação inválida");

        dialog.add(errorLabel);
        dialog.add(btnOk);  

        dialog.setLocationRelativeTo(SwingUtilities.getWindowAncestor(this));

        dialog.setModal(true);
        dialog.setVisible(true);

        //atualizar tela
        JFrame currentFrame = (JFrame) SwingUtilities.getWindowAncestor(this);
        currentFrame.remove(((JButton)evt.getSource()).getParent());        
        currentFrame.add(((JButton)evt.getSource()).getParent());
        currentFrame.revalidate();
        currentFrame.repaint();
    }
    
