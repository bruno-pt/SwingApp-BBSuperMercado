    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        JFrame currentFrame = (JFrame) SwingUtilities.getWindowAncestor(this);
        
        boolean produtoCorreto = true;
        
        String nome = "";
        int quantidade = 0;
        float preco = 0;
        String setor = "";
        String errorString = "";
        //Nome
        try{
            nome = tfNome.getText();
            if(nome.length() > 30){
                errorString = "Nome do produto muito grande! Máx 30 caracteres!";
                throw new RuntimeException(errorString);
            }
        }catch(RuntimeException e){ //erro de quantidade            
            jDialogError(errorString, evt);
            produtoCorreto = false;
        }
        
        //Preco
        try{
            preco = Float.parseFloat(tfPreco.getText());
        }catch(NumberFormatException e){
            errorString = "Preço inválido! Não é permitido Caracteres!";
            jDialogError(errorString, evt);
            produtoCorreto = false;
        }
        
        //Quantidade
        try{
            quantidade = Integer.parseInt(tfQuantidade.getText());
        }catch(NumberFormatException e){
            errorString = "Quantidade inválida! Não é permitido valores decimais!";
            jDialogError(errorString, evt);
            produtoCorreto = false;
        }
        
        if(produtoCorreto){
            Produto produto = new Produto(nome, quantidade, preco);

            setor = tfSetor.getText();

            Gerente.cadastrarProduto(produto, setor);

            JDialog dialog = new JDialog();
                dialog.setSize(250, 100);
                dialog.setLayout(new FlowLayout());
                dialog.setDefaultCloseOperation(JDialog.DISPOSE_ON_CLOSE);

                JLabel labelAddedProduto = new JLabel("Produto "+nome+" adicionado com Sucesso!");
                JButton btnOk = new JButton("OK");                    

                btnOk.addActionListener(new ActionListener(){
                    @Override
                    public void actionPerformed(ActionEvent e) {                
                        dialog.dispose();
                        tfNome.setText("");
                        tfPreco.setText("");
                        tfQuantidade.setText("");
                        tfSetor.setText("");

                        //atualizar tela
                        currentFrame.remove(((JButton)evt.getSource()).getParent());        
                        currentFrame.add(((JButton)evt.getSource()).getParent());
                        currentFrame.revalidate();
                        currentFrame.repaint();
                    }
                }); 

                dialog.setTitle("Produto adicionado!");

                dialog.add(labelAddedProduto);
                dialog.add(btnOk);  

                dialog.setLocationRelativeTo(SwingUtilities.getWindowAncestor(this));

                dialog.setModal(true);
                dialog.setVisible(true);
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

