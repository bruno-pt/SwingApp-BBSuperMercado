    public finalizarCarrinhoPanel(JFrame mainFrame) {        
        
        float valorTotalCompra = 0;
        Iterator<Produto> itCarrinho = Cliente.carrinho.iterator();
        
        Collections.shuffle(App.funcionarios);

        int i = 0;
        for (Caixa caixa : App.caixas) {
            caixa.setFuncionario(App.funcionarios.get(i));
            i++;
        }
        
        while(itCarrinho.hasNext()) {
            Produto produto = itCarrinho.next();
            valorTotalCompra += produto.getPrecoUni()* produto.getQuantidadeCompra();
        }
        //System.out.println(App.caixas.get(0).getFuncionario().getNome());        
        
        initComponents();
        
        mainFrame.setTitle("BB Super-Mercado | $Cliente - Finalizar carrinho");

        btnFuncionario1.setText("Caixa 1 - " + App.caixas.get(0).getFuncionario().getNome());
        btnFuncionario2.setText("Caixa 2 - " + App.caixas.get(1).getFuncionario().getNome());
        btnFuncionario3.setText("Caixa 3 - " + App.caixas.get(2).getFuncionario().getNome());
        labelValorCompra.setText("Valor da compra: R$ " + valorTotalCompra);
    }

