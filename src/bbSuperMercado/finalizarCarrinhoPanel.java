    private void dialogCompraFinalizada(float valorTotalCompra, java.awt.event.ActionEvent evt){
        
        //atualizar tela
        JFrame currentFrame = (JFrame) SwingUtilities.getWindowAncestor(this);
        currentFrame.remove(((JButton)evt.getSource()).getParent());  
        
        Cliente.corredorAtual = 0;
        Cliente.setorAtual = null;
        
        JFrame loginFrame = new loginFrame();
        
        currentFrame.dispose();
        loginFrame.setVisible(true);
        
        //JDialog
        JDialog dialog = new JDialog();
        dialog.setSize(250, 130);
        dialog.setLayout(new FlowLayout());
        dialog.setDefaultCloseOperation(JDialog.DISPOSE_ON_CLOSE);
        
        JLabel labelCompraFinalizada = new JLabel("Compra finalizada com sucesso!");
        JLabel labelValorTotalCompra = new JLabel("Valor: R$ " + valorTotalCompra);
        JButton btnOk = new JButton("OK");                    
        
        btnOk.addActionListener(new ActionListener(){
            @Override
            public void actionPerformed(ActionEvent e) {                
                dialog.dispose();                
            }
        }); 
        
        dialog.setTitle("Obrigado, volte sempre!");
        
        dialog.add(labelCompraFinalizada);
        dialog.add(labelValorTotalCompra);  
        dialog.add(btnOk);
        
        dialog.setLocationRelativeTo(SwingUtilities.getWindowAncestor(this));
        
        dialog.setModal(true);
        dialog.setVisible(true);             
    }
    
